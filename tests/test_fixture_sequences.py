"""
Tests that read from synthetic FASTA fixture files in tests/fixtures/sequences/.

Each test loads a fixture file from disk, calls find_orfs_candidates(), and
verifies that the detected ORFs match the ground-truth constants in conftest.py.

All fixtures are generated by tests/fixtures/sequences/generate_fixtures.py.
"""

import pytest
from pathlib import Path

from src.traditional_methods import find_orfs_candidates
from tests.conftest import (
    FIXTURES_DIR,
    SINGLE_ORF_START,
    SINGLE_ORF_END,
    SINGLE_ORF_LENGTH,
    SINGLE_ORF_STRAND,
    MULTI_ORF_COORDS,
    REVERSE_ORF_GENOME_START,
    REVERSE_ORF_GENOME_END,
    REVERSE_ORF_LENGTH,
    REVERSE_ORF_STRAND,
    LARGE_ORF_COUNT,
    LARGE_ORF_LENGTH,
    LARGE_ORF_COORDS,
)


def _load(filename: str) -> str:
    """Read a fixture FASTA and return the bare sequence (no header, no newlines)."""
    path = FIXTURES_DIR / filename
    lines = path.read_text().splitlines()
    return "".join(line for line in lines if not line.startswith(">"))


def _fwd(orfs):
    return [o for o in orfs if o["strand"] == "forward"]


def _rev(orfs):
    return [o for o in orfs if o["strand"] == "reverse"]


# ---------------------------------------------------------------------------
# synthetic_single_orf.fasta
# ---------------------------------------------------------------------------


class TestSingleOrfFixture:
    @pytest.fixture(scope="class")
    def orfs(self):
        seq = _load("synthetic_single_orf.fasta")
        return find_orfs_candidates(seq, min_length=100)

    def test_fixture_file_exists(self):
        assert (FIXTURES_DIR / "synthetic_single_orf.fasta").exists()

    def test_exactly_one_orf_detected(self, orfs):
        assert len(orfs) == 1

    def test_orf_is_on_forward_strand(self, orfs):
        assert orfs[0]["strand"] == SINGLE_ORF_STRAND

    def test_orf_start_matches_ground_truth(self, orfs):
        assert orfs[0]["start"] == SINGLE_ORF_START

    def test_orf_end_matches_ground_truth(self, orfs):
        assert orfs[0]["end"] == SINGLE_ORF_END

    def test_orf_length_matches_ground_truth(self, orfs):
        assert orfs[0]["length"] == SINGLE_ORF_LENGTH

    def test_orf_starts_with_atg(self, orfs):
        assert orfs[0]["start_codon"] == "ATG"


