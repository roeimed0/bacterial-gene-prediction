class TestMultiOrfFixture:
    @pytest.fixture(scope="class")
    def orfs(self):
        seq = _load("synthetic_multi_orf.fasta")
        return find_orfs_candidates(seq, min_length=100)

    def test_fixture_file_exists(self):
        assert (FIXTURES_DIR / "synthetic_multi_orf.fasta").exists()

    def test_five_orfs_detected(self, orfs):
        assert len(orfs) == len(MULTI_ORF_COORDS)

    def test_all_orfs_on_forward_strand(self, orfs):
        assert all(o["strand"] == "forward" for o in orfs)

    @pytest.mark.parametrize("idx,expected", list(enumerate(MULTI_ORF_COORDS)))
    def test_orf_start(self, orfs, idx, expected):
        fwd = sorted(_fwd(orfs), key=lambda o: o["start"])
        assert fwd[idx]["start"] == expected["start"]

    @pytest.mark.parametrize("idx,expected", list(enumerate(MULTI_ORF_COORDS)))
    def test_orf_end(self, orfs, idx, expected):
        fwd = sorted(_fwd(orfs), key=lambda o: o["start"])
        assert fwd[idx]["end"] == expected["end"]

    @pytest.mark.parametrize("idx,expected", list(enumerate(MULTI_ORF_COORDS)))
    def test_orf_length(self, orfs, idx, expected):
        fwd = sorted(_fwd(orfs), key=lambda o: o["start"])
        assert fwd[idx]["length"] == expected["length"]

    def test_all_orfs_start_with_atg(self, orfs):
        assert all(o["start_codon"] == "ATG" for o in orfs)


